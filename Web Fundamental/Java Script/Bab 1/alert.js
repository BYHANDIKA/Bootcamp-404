// cara Membuat Alert
// Function onkeypress ditampilkan ketika ada tombol yang ditekan pada input field
function OnKeypress(){
    alert("Password harus berupa gabungan dari beberapa huruf, angka, dan symbol");
}
//Function onClick ditampilkan ketika elemen yang terkait di klik
function OnClick(){
    alert("Terimakasih telah menekan tombol ini hehe");
}
//Function onsubmit ditampilkan ketika form disubmit
function OnSubmit(){
    alert("Data Berhasil disubmit");
}
// Function onchange ditampilkan ketika nilai pada radio button berubah
function OnChange(jk){
    alert("Jenis Kelamin Anda adalah "+jk);
}
// Fungsi onload ditampilkan ketika halaman sudah selesai diproses
function OnLoad(){
    alert("Halaman sudah Selesai Dimuat");
}